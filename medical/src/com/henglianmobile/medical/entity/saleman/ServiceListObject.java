package com.henglianmobile.medical.entity.saleman;

public class ServiceListObject {

	private String dcRealName;
	private String serviceNum;
	private String sumDay;
	public String getDcRealName() {
		return dcRealName;
	}
	public void setDcRealName(String dcRealName) {
		this.dcRealName = dcRealName;
	}
	public String getServiceNum() {
		return serviceNum;
	}
	public void setServiceNum(String serviceNum) {
		this.serviceNum = serviceNum;
	}
	public String getSumDay() {
		return sumDay;
	}
	public void setSumDay(String sumDay) {
		this.sumDay = sumDay;
	}
	
}
